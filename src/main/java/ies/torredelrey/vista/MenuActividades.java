    private void actualizarTablaActividad() { //funcion para actualizar las lista
    // obtener todas las actividades
        List<Actividad> actividad = activ.findAllActividad();
        
    // mostramos las cabezas de la tabla
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Nombre");
        model.addColumn("Nº plazas");
    //rellenamos las actividades    
        for (Actividad actividades : actividad) {
            model.addRow(new Object[]{
                actividades.getId(),
                actividades.getNombreActividad(),
                actividades.getNumeroPlazas(),
                      
            });
        }

        tblActividades.setModel(model);
    }
