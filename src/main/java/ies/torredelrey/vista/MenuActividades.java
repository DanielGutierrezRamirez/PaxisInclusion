    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //la fila de la tabla seleccionada es la que se elimina
        int selectedRow = tblActividades.getSelectedRow();

        if (selectedRow != -1) {
            int opcion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar la Actividad?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_OPTION) {
            // Obtenemos la lista de usuarios y el usuario seleccionado
            List<Actividad> actividad = activ.findAllActividad(); 
            Actividad actividades = actividad.get(selectedRow);

            try {
                activ.destroy(actividades.getId()); // eliminamos y actualizamos
                actualizarTablaActividad();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar la actividad, esta actividad contiene reservas. Primero elimina las reservas. ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una actividad para eliminar", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

