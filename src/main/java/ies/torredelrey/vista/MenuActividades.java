    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        int selectedRow = tblActividades.getSelectedRow();

        if (selectedRow != -1) {
            // obtenemos la lista de actividades y selecionamos la que se va a modificar
            List<Actividad> actividadList = activ.findAllActividad(); 
            Actividad actividad = actividadList.get(selectedRow);

            if (actividad != null) {
                // si existe nos vamos a la ventana de editar
                EditarActividad editarActividad = new EditarActividad(actividad);
                editarActividad.setVisible(true);
                this.dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "La actividad seleccionada no es válida", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una actividad para editar", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

