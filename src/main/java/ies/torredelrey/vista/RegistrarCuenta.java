    /**
     * Método que se ejecuta al presionar el botón "Guardar".
     * Valida los datos ingresados y crea un nuevo administrador en la base de datos.
     * 
     * @param evt El evento de acción.
     */
    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        boolean salir = false;
        //patrones
        String dniPattern = "^[0-9]{8}[a-zA-Z]$";
        Pattern patterndni = Pattern.compile(dniPattern);
        String emailPattern = "^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";
        Pattern patternemail = Pattern.compile(emailPattern);
        
        //validaciones de campos en blanco y patrones
        if (txtDni.getText().isBlank() || txtDni.getText().isEmpty() || !patterndni.matcher(txtDni.getText()).matches()) {
            salir = true;
            JOptionPane.showMessageDialog(this, "El formato del DNI es incorrecto. Debe ser 00000000X.");
        }
        if (txtEmail.getText().isBlank() || txtEmail.getText().isEmpty() || !patternemail.matcher(txtEmail.getText()).matches()) {
            salir = true;
            JOptionPane.showMessageDialog(this, "El formato del Email es incorrecto. Debe ser aaa@aaa.aa.");
        }
        if (txtNombre.getText().isBlank() || txtNombre.getText().isEmpty()) {
            salir = true;
            JOptionPane.showMessageDialog(this, "Introduce un Nombre.");
        }
        if (txtApellidos.getText().isBlank() || txtApellidos.getText().isEmpty()) {
            salir = true;
            JOptionPane.showMessageDialog(this, "Introduce un Apellido.");
        }
        if (txtContrasena1.getText().isBlank() || txtContrasena1.getText().isEmpty()) {
            salir = true;
            JOptionPane.showMessageDialog(this, "Introduce una Contraseña.");
        }
        if (!txtContrasena1.getText().equals(txtContrasena2.getText()) || txtContrasena2.getForeground() == Color.red) {
            salir = true;
            JOptionPane.showMessageDialog(this, "Las contraseñas deben ser iguales.");
        }

        if (salir) return;

        // Verificar si el DNI ya está registrado
        if (dniExistente(txtDni.getText())) {
            JOptionPane.showMessageDialog(this, "El DNI ya está registrado.");
            return;
        }

        // Crear un nuevo administrador y guardarlo
        Administrador nuevoAdministrador = new Administrador();
        nuevoAdministrador.setDni(txtDni.getText());
        nuevoAdministrador.setNombre(txtNombre.getText());
        nuevoAdministrador.setApellidos(txtApellidos.getText());
        nuevoAdministrador.setEmail(txtEmail.getText());
        nuevoAdministrador.setContraseña(txtContrasena1.getText());

        try {
            admin.create(nuevoAdministrador);
            JOptionPane.showMessageDialog(this, "Usuario agregado correctamente.");
            this.dispose();
            Login login = new Login(); 
            login.setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(RestablecerContrasena.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al agregar el usuario.");
        }
        
        
    }//GEN-LAST:event_btnGrabarActionPerformed
