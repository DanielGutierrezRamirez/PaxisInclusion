    //funcion para comprobar si ya exite el dni
    private boolean dniExistente(String dni) {
        ArrayList<Administrador> administrador = new ArrayList<>(admin.findAdministradorEntities());
        for (Administrador a : administrador) {
            if (a.getDni().equals(dni)) {
                return true;
            }
        }
        return false;
    }

